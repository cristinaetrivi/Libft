#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (!new)
		return (new);
	new -> content = content;
	new -> next = (t_list *)(void *)0;
	return (new);
}
